void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  uint32_t now = HAL_GetTick();
  if(now - last_press < 333)
    return;

  last_press = now;
  if(GPIO_Pin == BUTTON_1_Pin)
   is_button_1_pressed = 1;
  else if(GPIO_Pin == BUTTON_2_Pin)
    is_button_2_pressed = 1;
  else if(GPIO_Pin == BUTTON_3_Pin)
    is_button_3_pressed = 1;
  else if(GPIO_Pin == BUTTON_4_Pin)
    is_button_4_pressed = 1;
  else if(GPIO_Pin == BUTTON_5_Pin)
    is_button_5_pressed = 1;
}

